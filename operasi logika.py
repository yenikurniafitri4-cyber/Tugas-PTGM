# operasi logika 

# NOT
a = False
c = not a
print ('data a =',a)
print ('data c =',c)



# OR (jika salah satu true maka hasilnya true)
a = False
b = False
c = a or b
print(a,'OR',b,'=',c)

# OR 
a = True 
b = False
c = a or b
print(a,'OR',b,'=',c)



# AND (jika dua buah nilai true,maka hasilnya true)
a = False
b = False
c = a and b
print(a,'AND',b,'=',c)

# AND
a = True
b = False
c = a and b
print(a,'AND',b,'=',c)

# AND
a = True
b = True
c = a and b
print(a,'AND',b,'=',c)

