# operasi ==
hasil = a == b
print(a,'==',b,'=',hasil)

# operasi !=
hasil = a != b
print(a,'!=',b,'=',hasil)

# operasi >
hasil = a > b
print(a,'>',b,'=',hasil)

# operasi <
hasil = a < b
print(a,'<',b,'=',hasil)

# operasi >=
hasil = a >= b
print(a,'>=',b,'=',hasil)

# operasi <=
hasil = a <= b
print(a,'<=',b,'=',hasil)